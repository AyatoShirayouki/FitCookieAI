		[HttpPost("Charge")]
		public async Task<IActionResult> Charge(PaymentDTO paymentRequest, [FromHeader] string token, [FromHeader] string refreshToken)
		{
			if (token != null && refreshToken != null)
            {
				tokenRequest.Token = token;
				tokenRequest.RefreshToken = refreshToken;

				JwtResult jwtUserToken = await _usersAuthentication.RefreshUserToken(tokenRequest);

				if (jwtUserToken.JwtSuccess == true)
				{
                    try
                    {
						var options = new ChargeCreateOptions
						{
							Amount = paymentRequest.Amount,
							Currency = paymentRequest.Currency,
							Description = paymentRequest.Description,
							Source = paymentRequest.Source,
						};

						var service = new ChargeService();
						Charge charge = service.Create(options);

						if (charge.Paid)
						{
							response.Code = 201;
							await PaymentsManagementService.Save(paymentRequest);
							response.Body = new { message = "Payment succeeded", charge };
						}
						else
						{
							response.Code = 200;
                            response.Body = "Failed";
							response.Error = new { message = "Payment failed", charge };
						}
					}
					catch (Exception ex)
					{
						response.Code = 500;
                        response.Body = "Failed";
						response.Error = new { message = "Error processing payment", details = ex.Message };
					}

					HttpContext.Response.Headers.Add("token", jwtUserToken.Token);
					HttpContext.Response.Headers.Add("refreshToken", jwtUserToken.RefreshToken);

					response.JwtSuccess = jwtUserToken.JwtSuccess;
					response.JwtErrors = jwtUserToken.JwtErrors;
				}
				else
				{
					response.Code = 400;
					response.Error = "Missing data - Token or refresh token is null or invalid!";
				}
			}

			return new JsonResult(response);
		}

